package io.jboot.web.attachment;

import java.io.Serializable;

/**
 * @author michael yang (fuhai999@gmail.com)
 * @Date: 2020/3/1
 */
public class AttachmentDeleteAction implements Serializable {

    private static final long serialVersionUID = 1L;

    private String id;
    private String path;

    public AttachmentDeleteAction(String id, String path) {
        this.id = id;
        this.path = path;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }
}
